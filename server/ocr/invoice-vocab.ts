/**
 * 헤더 셀 오독 정규화 (표준명으로 교체)
 * 매칭되면 std 반환, 아니면 원본 반환
 */
export function normalizeHeaderCell(text: string): string {
  const t = (text ?? "").trim();
  for (const [re, std] of HEADER_MISREAD_MAP) {
    if (re.test(t)) return std;
  }
  return t;
}

/**
 * 셀 텍스트가 헤더 노이즈인지 판정
 */
export function isHeaderNoise(text: string): boolean {
  const t = (text ?? "").trim();
  if (!t) return true;
  if (t.length === 1 && HEADER_NOISE_SINGLES.has(t)) return true;
  return false;
}

/**
 * 행이 소계/합계/세액 행인지 판정
 */
export function isTotalRow(cells: (string | null | number)[]): boolean {
  const joined = cells.filter(c => c != null).map(c => String(c)).join(" ");
  return TOTAL_ROW_RE.test(joined);
}
